package day2.methods;

public class Exmpl5 {
	
	public static int add(int a,int b) {
		
		int c=a+b;
		return c;
	}
	
	public static int sub(int a,int b) {//using static method because it is easy to call in main
		
		int c=a-b;
		return c;
	}

	public static int mult(int a,int b) {
		int c=a*b;
		return c;
	}

}
