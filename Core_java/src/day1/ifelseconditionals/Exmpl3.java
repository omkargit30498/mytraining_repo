package day1.ifelseconditionals;

public class Exmpl3 {

	public static void main(String[] args) {
		//elseif use
		int time = 22;
		if (time < 10) {
		  System.out.println("Good morning.");
		} else if (time < 20) {
		  System.out.println("Good day.");
		} else {
		  System.out.println("Good evening.");
		}

	}

}
