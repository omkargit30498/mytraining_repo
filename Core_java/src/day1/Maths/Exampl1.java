package day1.Maths;

public class Exampl1 {

	public static void main(String[] args) {
		int resultmax=Math.max(59, 51);
		System.out.println(resultmax);//maximum
		int resultmin=Math.min(58, 51);
		System.out.println(resultmin);//minimum
		
		double res3=Math.sqrt(64);
		System.out.println(res3);// squareroot
		
		int res4=Math.abs(-8);
		System.out.println(res4);//absolute
		
		double res5=Math.random();
		System.out.println(res5);
		
		int res6=(int)(Math.random()*101);
		System.out.println(res6);
				
	}

}
